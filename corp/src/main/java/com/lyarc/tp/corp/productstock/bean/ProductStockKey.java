package com.lyarc.tp.corp.productstock.bean;

public class ProductStockKey {
	private String storageId;
	private String materId;
	private String materName;

	private String casting;

	public String getStorageId() {
		return storageId;
	}

	public void setStorageId(String storageId) {
		this.storageId = storageId;
	}

	public String getMaterId() {
		return materId;
	}

	public void setMaterId(String materId) {
		this.materId = materId;
	}

	public String getMaterName() {
		return materName;
	}

	public void setMaterName(String materName) {
		this.materName = materName;
	}

	public String getCasting() {
		return casting;
	}

	public void setCasting(String casting) {
		this.casting = casting;

	}

	public ProductStockKey() {
	}

	public ProductStockKey(String storageId, String materId, String casting) {
		this.storageId = storageId;
		this.materId = materId;
		this.casting = casting;

	}

	public static ProductStockKey create(String storageId, String materId, String casting) {
		return new ProductStockKey(storageId, materId, casting);
	}

}
