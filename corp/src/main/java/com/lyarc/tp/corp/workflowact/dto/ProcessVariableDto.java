package com.lyarc.tp.corp.workflowact.dto;

import java.io.Serializable;

/**
 *
 * @description 流程变量dto
 * @param
 * @return
 * @author wangting
 * @date 2018/5/14 0014 9:05
 *
 */
public class ProcessVariableDto implements Serializable {

   private static final long serialVersionUID = -5343616931041747907L;
   private String id;

   private String variableName;
   private String variableTypeName;
   private Object value;

   public String getId() {
       return id;
   }

   public void setId(String id) {
       this.id = id;
   }

   public String getVariableName() {
       return variableName;
   }

   public void setVariableName(String variableName) {
       this.variableName = variableName;
   }

   public String getVariableTypeName() {
       return variableTypeName;
   }

   public void setVariableTypeName(String variableTypeName) {
       this.variableTypeName = variableTypeName;
   }

   public Object getValue() {
       return value;
   }

   public void setValue(Object value) {
       this.value = value;
   }
}
